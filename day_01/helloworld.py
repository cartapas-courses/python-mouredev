# Checking data types
print(type(10))                  # Int
print(type(9.8))                 # Float
print(type(3.14))                # Float
print(type(4 - 4j))              # Complex number
print(type(['Carlos', 'Python', 'Colombia']))           # List
print(type('Carlos'))            # String
