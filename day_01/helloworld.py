# Math operations
print(3 + 4)             # addition(+)
print(3 - 4)             # subtraction(-)
print(3 * 4)             # multiplication(*)
print(3 % 4)             # modulus(%)
print(3 / 4)             # division(/)
print(3 ** 4)            # exponential(**)
print(3 // 4)            # Floor division operator(//)

# Write strings
